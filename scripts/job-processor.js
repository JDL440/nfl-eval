import { Worker } from 'bullmq';
import Redis from 'ioredis';
import { v4 as uuid } from 'uuid';
import db from '../src/db.js';
import { countTokens, calculateCost, extractTokenUsageFromResponse } from '../src/tokenCounter.js';

const redis = new Redis({
  host: process.env.REDIS_HOST || 'localhost',
  port: process.env.REDIS_PORT || 6379,
});

// Mock Anthropic API call (for local testing without real API key)
async function callClaudeHaiku(systemPrompt, userMessage) {
  // In production, this would call the real Anthropic API
  const estimatedTokens = {
    input_tokens: countTokens(systemPrompt + userMessage),
    output_tokens: 500, // Estimate for typical article draft
  };

  return {
    content: [
      {
        type: 'text',
        text: `[DRAFT ARTICLE]\n\nBased on the following news: ${userMessage}\n\nThis is a placeholder draft. In production, this would be generated by Claude Haiku.\n\nKey insights:\n- Significant transaction in the NFL\n- Impacts team dynamics\n- Requires editorial review before publication`,
      },
    ],
    usage: estimatedTokens,
  };
}

async function processDraftJob(job) {
  const jobId = job.id;
  const jobData = JSON.parse(job.data.data);

  console.log(`\n📝 Processing draft job: ${jobId}`);
  console.log(`   Player: ${jobData.player} (${jobData.position})`);
  console.log(`   To: ${jobData.to_team}`);

  try {
    // Update job state to processing
    const updateStmt = db.prepare('UPDATE jobs SET state = ?, updated_at = CURRENT_TIMESTAMP WHERE id = ?');
    updateStmt.run('processing', jobId);

    // Audit log: job started
    const auditId = `audit-${uuid()}`;
    const auditStmt = db.prepare(`
      INSERT INTO audit_log (id, job_id, action, actor, details, created_at)
      VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP)
    `);
    auditStmt.run(auditId, jobId, 'processing', 'job-processor', JSON.stringify({ model: 'claude-3-5-haiku' }));

    // Call Claude Haiku to draft article
    const systemPrompt = `You are an expert NFL analyst. Draft a compelling article about this transaction.`;
    const userMessage = `${jobData.player} (${jobData.position}) signed with ${jobData.to_team}. Deal: ${JSON.stringify(jobData.deal)}`;

    const response = await callClaudeHaiku(systemPrompt, userMessage);

    // Extract token usage
    const tokenUsage = extractTokenUsageFromResponse(response);
    const cost = calculateCost('claude-3-5-haiku', tokenUsage.input_tokens, tokenUsage.output_tokens);

    // Insert token usage record
    const tokenId = `token-${uuid()}`;
    const tokenStmt = db.prepare(`
      INSERT INTO token_usage (id, job_id, model, input_tokens, output_tokens, cost, created_at)
      VALUES (?, ?, ?, ?, ?, ?, CURRENT_TIMESTAMP)
    `);
    tokenStmt.run(tokenId, jobId, 'claude-3-5-haiku', tokenUsage.input_tokens, tokenUsage.output_tokens, cost);

    // Store result
    const result = {
      draft_text: response.content[0].text,
      token_usage: tokenUsage,
      cost: cost,
    };

    // Update job with result and complete state
    const completeStmt = db.prepare(`
      UPDATE jobs 
      SET state = ?, result = ?, token_usage = ?, completed_at = CURRENT_TIMESTAMP, updated_at = CURRENT_TIMESTAMP 
      WHERE id = ?
    `);
    completeStmt.run('completed', JSON.stringify(result), JSON.stringify(tokenUsage), jobId);

    // Audit log: job completed
    const completeAuditId = `audit-${uuid()}`;
    auditStmt.run(
      completeAuditId,
      jobId,
      'completed',
      'job-processor',
      JSON.stringify({ tokens: tokenUsage, cost: cost })
    );

    console.log(`✅ Job completed: ${jobId}`);
    console.log(`   Tokens: ${tokenUsage.input_tokens} input, ${tokenUsage.output_tokens} output`);
    console.log(`   Cost: $${cost.toFixed(4)}`);

    return result;
  } catch (err) {
    console.error(`❌ Job failed: ${jobId}`, err.message);

    // Update job state to failed
    const failStmt = db.prepare(`
      UPDATE jobs 
      SET state = ?, error_message = ?, retry_count = retry_count + 1, updated_at = CURRENT_TIMESTAMP 
      WHERE id = ?
    `);
    failStmt.run('failed', err.message, jobId);

    // Audit log: job failed
    const failAuditId = `audit-${uuid()}`;
    auditStmt.run(failAuditId, jobId, 'failed', 'job-processor', JSON.stringify({ error: err.message }));

    throw err;
  }
}

async function startWorker() {
  console.log('🚀 Starting BullMQ worker for article-draft queue...');

  const worker = new Worker('article-draft', processDraftJob, {
    connection: redis,
    concurrency: 1, // Process one job at a time locally
  });

  worker.on('completed', (job) => {
    console.log(`✅ Worker completed job: ${job.id}`);
  });

  worker.on('failed', (job, err) => {
    console.error(`❌ Worker failed job: ${job.id}`, err.message);
  });

  worker.on('error', (err) => {
    console.error('Worker error:', err);
  });

  console.log('✅ Worker ready. Waiting for jobs...');

  // Graceful shutdown
  process.on('SIGINT', async () => {
    console.log('\n🛑 Shutting down worker...');
    await worker.close();
    await redis.quit();
    process.exit(0);
  });
}

// CLI execution
if (import.meta.url === `file://${process.argv[1]}`) {
  startWorker().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}

export { processDraftJob, startWorker };
